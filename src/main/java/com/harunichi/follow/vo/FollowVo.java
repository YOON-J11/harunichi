package com.harunichi.follow.vo;

public class FollowVo {
	private String follower_id;
	private String followee_id;
	private String follow_date;
	
	public String getFollower_id() {
		return follower_id;
	}
	public void setFollower_id(String follower_id) {
		this.follower_id = follower_id;
	}
	public String getFollowee_id() {
		return followee_id;
	}
	public void setFollowee_id(String followee_id) {
		this.followee_id = followee_id;
	}
	public String getFollow_date() {
		return follow_date;
	}
	public void setFollow_date(String follow_date) {
		this.follow_date = follow_date;
	}	
	
	
}
